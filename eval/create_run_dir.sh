#!/usr/bin/env bash

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(dirname "$SCRIPT_DIR")"
RUNS_ROOT="$SCRIPT_DIR/runs"

label="${1:-manual}"
safe_label="$(printf '%s' "$label" | tr ' /:' '___' | tr -cd '[:alnum:]_.-')"
timestamp="$(date -u +%Y%m%d_%H%M%S)"
run_dir="$RUNS_ROOT/${timestamp}_${safe_label}"

mkdir -p "$run_dir/raw" "$run_dir/derived" "$run_dir/logs" "$run_dir/manifests"

metadata_file="$run_dir/metadata.env"
status_file="$run_dir/git_status.txt"
commands_file="$run_dir/commands.sh"

command_exists() {
    command -v "$1" >/dev/null 2>&1
}

write_kv() {
    local key="$1"
    local value="$2"
    printf '%s=%q\n' "$key" "$value" >> "$metadata_file"
}

{
    echo "# Generated by eval/create_run_dir.sh"
    echo "# Source this file or parse it as shell-style key/value data."
} > "$metadata_file"

cd "$REPO_ROOT"

write_kv RUN_LABEL "$label"
write_kv RUN_TIMESTAMP_UTC "$timestamp"
write_kv REPO_ROOT "$REPO_ROOT"
write_kv GIT_COMMIT "$(git rev-parse HEAD)"
write_kv GIT_BRANCH "$(git rev-parse --abbrev-ref HEAD)"
write_kv HOSTNAME "$(hostname 2>/dev/null || echo unknown)"
write_kv OS_KERNEL "$(uname -sr)"
write_kv OS_MACHINE "$(uname -m)"
write_kv RUSTC_VERSION "$(rustc --version 2>/dev/null || echo unavailable)"
write_kv CARGO_VERSION "$(cargo --version 2>/dev/null || echo unavailable)"
write_kv GCC_VERSION "$(gcc --version 2>/dev/null | head -n 1 || echo unavailable)"
write_kv CLANG_VERSION "$(clang --version 2>/dev/null | head -n 1 || echo unavailable)"
write_kv Z3_VERSION "$(z3 --version 2>/dev/null || echo unavailable)"
write_kv HYPERFINE_VERSION "$(hyperfine --version 2>/dev/null || echo unavailable)"

if command_exists lscpu; then
    write_kv CPU_MODEL "$(lscpu | awk -F: '/Model name:/ {gsub(/^[ \t]+/, "", $2); print $2; exit}')"
fi

git status --short > "$status_file"
cp "$SCRIPT_DIR"/suites/*.txt "$run_dir/manifests/"
cp "$SCRIPT_DIR"/suites/README.md "$run_dir/manifests/"

cat > "$commands_file" <<EOF
#!/usr/bin/env bash

# Fill this file with the exact evaluation commands used for this run.
# Keep raw outputs in:
#   $run_dir/raw
# Keep derived summaries in:
#   $run_dir/derived
# Keep human-readable logs in:
#   $run_dir/logs
EOF

cat > "$run_dir/README.md" <<EOF
# Evaluation Run

- label: \`$label\`
- timestamp (UTC): \`$timestamp\`
- git commit: \`$(git rev-parse --short HEAD)\`

This directory is the root for one reproducible evaluation run.

Subdirectories:

- \`raw/\` raw benchmark outputs such as JSON and Hyperfine exports
- \`derived/\` CSV summaries and processed tables
- \`logs/\` human-readable logs
- \`manifests/\` frozen suite manifests copied at run creation time

Metadata is stored in \`metadata.env\`.
Git worktree state is stored in \`git_status.txt\`.
EOF

echo "$run_dir"
